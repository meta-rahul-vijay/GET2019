package question1;
/**
 *  This class is used to represent key value pair of data.
 * @author Rahul Vijay
 * Dated 08/07/2019
 */
public class KeyValuePair {
	
	public int key;
	public String value;
	
	/**
	 * This is constructor of keyvaluepair for initialization of data.
	 * @param key, key of data.
	 * @param value, value of data.
	 */
	KeyValuePair(int key, String value) {
		this.key = key;
		this.value = value;
		
	}

}
